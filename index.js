function relocate(buttonNum)
{
    switch(buttonNum) {
        case 1:
            window.location.href = "addPage/add.htm";
            break;
        case 2:
            window.location.href = "editPage/edit.htm";
            break;
        case 3:
            window.location.href = "findPage/find.htm";
            break;

      }
}